def basic_strategy(hand, dealer_card):
    player_total = hand.value
    dealer_value = dealer_card.value
    dealer_value = dealer_value if dealer_value <11 else 'A'

    double_possible = len(hand.cards) == 2
    index = 0 if double_possible else -1  ## On lit D ou H ou S dans les DH/DS

    if hand.is_pair():
        return BASIC_STRATEGY_PAIR[(hand.cards[0].value, dealer_value)][index]

    elif hand.has_ace():
        return BASIC_STRATEGY_SOFT[(player_total, dealer_value)][index]

    else:
        return BASIC_STRATEGY_HARD[(player_total, dealer_value)][index]


